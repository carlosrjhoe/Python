nomes = ['alice', 'david', 'carolina']

"""Vamos usar um laço for para exibir cada um dos nomes de uma lista"""
for nome in nomes:
    print(f'{nome}, that was a great trick!')
    print(f"I can't wait to see your next trick, {nome.title()}\n")

print(f'Thank you, everyone. That was a great {nome.title()} magic show!')

