name = "ada lovelace"
print(name.title())
# title() exibe cada palavra com uma letra maiúscula no início.

print(name.upper())
# upper() exibe todas palavras com uma letra maiúscula.

print(name.lower())
# lower() exibe todas palavras com letras minusculas.

